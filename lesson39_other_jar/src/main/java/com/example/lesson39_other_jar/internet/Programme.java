public class Programme {
    private String ldescribe;
    private String lessonType;
    private int lid;
    private String lname;
    private int nums;
    private int needStars;
    private String thumbUrl;
    private int tid;

    public String getLdescribe() {
        return ldescribe;
    }

    public void setLdescribe(String ldescribe) {
        this.ldescribe = ldescribe;
    }

    public String getLessonType() {
        return lessonType;
    }

    public void setLessonType(String lessonType) {
        this.lessonType = lessonType;
    }

    public int getLid() {
        return lid;
    }

    public void setLid(int lid) {
        this.lid = lid;
    }

    public String getLname() {
        return lname;
    }

    public void setLname(String lname) {
        this.lname = lname;
    }

    public int getNums() {
        return nums;
    }

    public void setNums(int nums) {
        this.nums = nums;
    }

    public int getNeedStars() {
        return needStars;
    }

    public void setNeedStars(int needStars) {
        this.needStars = needStars;
    }

    public String getThumbUrl() {
        return thumbUrl;
    }

    public void setThumbUrl(String thumbUrl) {
        this.thumbUrl = thumbUrl;
    }

    public int getTid() {
        return tid;
    }

    public void setTid(int tid) {
        this.tid = tid;
    }
}
